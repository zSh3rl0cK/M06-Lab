# Fun√ß√µes para calcular os erros
def erroAbsoluto(vt, vm):
    return abs(vt - vm)

def erroRelativo(vt, vm):
    ea = erroAbsoluto(vt, vm)
    return ea / abs(vm)

def erroPercentual(vt, vm):
    er = erroRelativo(vt, vm)
    return er * 100

# Calcular e imprimir os resultados
for i in range(len(df_ordenado)):
    aluno = df_ordenado["Alunos"][i]
    vm = df_ordenado["Valor Medido (K*J)"][i]
    ea = erroAbsoluto(valor_teorico, vm)
    er = erroRelativo(valor_teorico, vm)
    ep = erroPercentual(valor_teorico, vm)

    print(f"Aluno: {aluno}")
    print(f"Valor Medido: {vm}")
